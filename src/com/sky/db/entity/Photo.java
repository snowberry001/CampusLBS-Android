package com.sky.db.entity;

public class Photo {

	
	private String photoId;
	private String photoTourId;
	private String photoTakenTime;
	private String photoLocation;
	private String photoDesc;
	private String photoUrl;
	private String photoAccurateLocation;
	private String photoVoiceUrl;
	
	
	public String getPhotoId() {
		return photoId;
	}
	public void setPhotoId(String photoId) {
		this.photoId = photoId;
	}
	public String getPhotoTourId() {
		return photoTourId;
	}
	public void setPhotoTourId(String photoTourId) {
		this.photoTourId = photoTourId;
	}
	public String getPhotoTakenTime() {
		return photoTakenTime;
	}
	public void setPhotoTakenTime(String photoTakenTime) {
		this.photoTakenTime = photoTakenTime;
	}
	public String getPhotoLocation() {
		return photoLocation;
	}
	public void setPhotoLocation(String photoLocation) {
		this.photoLocation = photoLocation;
	}
	public String getPhotoDesc() {
		return photoDesc;
	}
	public void setPhotoDesc(String photoDesc) {
		this.photoDesc = photoDesc;
	}
	public String getPhotoUrl() {
		return photoUrl;
	}
	public void setPhotoUrl(String photoUrl) {
		this.photoUrl = photoUrl;
	}
	public String getPhotoAccurateLocation() {
		return photoAccurateLocation;
	}
	public void setPhotoAccurateLocation(String photoAccurateLocation) {
		this.photoAccurateLocation = photoAccurateLocation;
	}
	public String getPhotoVoiceUrl() {
		return photoVoiceUrl;
	}
	public void setPhotoVoiceUrl(String photoVoiceUrl) {
		this.photoVoiceUrl = photoVoiceUrl;
	}
	
}
