package com.sky.db.entity;

public class Tour {

	
	private String tourId;
	private String tourStartTime;
	private String tourEndTime;
	private String tourTitle;
	private String tourLocation;
	private String tourAccurateLocation;
	private String tourFileUrl;
	
	public String getTourId() {
		return tourId;
	}
	public void setTourId(String tourId) {
		this.tourId = tourId;
	}
	public String getTourStartTime() {
		return tourStartTime;
	}
	public void setTourStartTime(String tourStartTime) {
		this.tourStartTime = tourStartTime;
	}
	public String getTourEndTime() {
		return tourEndTime;
	}
	public void setTourEndTime(String tourEndTime) {
		this.tourEndTime = tourEndTime;
	}
	public String getTourTitle() {
		return tourTitle;
	}
	public void setTourTitle(String tourTitle) {
		this.tourTitle = tourTitle;
	}
	public String getTourLocation() {
		return tourLocation;
	}
	public void setTourLocation(String tourLocation) {
		this.tourLocation = tourLocation;
	}
	public String getTourAccurateLocation() {
		return tourAccurateLocation;
	}
	public void setTourAccurateLocation(String tourAccurateLocation) {
		this.tourAccurateLocation = tourAccurateLocation;
	}
	public String getTourFileUrl() {
		return tourFileUrl;
	}
	public void setTourFileUrl(String tourFileUrl) {
		this.tourFileUrl = tourFileUrl;
	}
	
	
}
